// Этап по номеру недели (1..10)
export function stageForWeek(week) {
  return STAGES.find(s => s.weeks.includes(week)) || STAGES[STAGES.length - 1];
}

// Состояние программы относительно сегодня
export function planState(startStr) {
  const start = new Date((startStr || PLAN_START_DEFAULT) + 'T00:00:00');
  const now = new Date();
  const today0 = new Date(now.getFullYear(), now.getMonth(), now.getDate());
  const msDay = 86400000;
  const diffDays = Math.floor((today0 - start) / msDay);

  if (diffDays < 0) {
    return { status: 'before', daysToStart: -diffDays, week: 1, start };
  }
  const week = Math.floor(diffDays / 7) + 1;
  if (week > PLAN_WEEKS) return { status: 'done', week: PLAN_WEEKS, start };

  const dow = now.getDay();                    // 0=вс
  const dayKey = DAY_KEYS[dow === 0 ? 6 : dow - 1];
  return { status: 'active', week, dayKey, start };
}
