#!/usr/bin/env bash
# ════════════════════════════════════════════════════════════════════════════
#  ТОЧКА ВОЗВРАТА ВИЗУАЛА — LIFE OS
#  Метка: ui-stable-v41  (рабочий дизайн до экспериментов с редизайном)
#
#  Запуск:   bash ОТКАТ-ВИЗУАЛА.sh
#  Что делает: возвращает ВЕСЬ код к состоянию на момент метки и
#              перезаписывает ветку main → Vercel автоматически передеплоит
#              старую (рабочую) версию через ~1 минуту.
# ════════════════════════════════════════════════════════════════════════════
set -e

TAG="ui-stable-v41"

echo ""
echo "⚠️  ОТКАТ к точке: $TAG"
echo "   Это вернёт весь проект к рабочему визуалу и перезапишет историю main."
echo "   Все изменения, сделанные ПОСЛЕ метки, будут отброшены."
echo ""
read -p "Точно откатываемся? Введи большими буквами YES: " ok

if [ "$ok" != "YES" ]; then
  echo "❌ Отменено. Ничего не тронуто."
  exit 1
fi

git fetch --tags
git reset --hard "$TAG"
git push --force origin main

echo ""
echo "✅ Готово. Код вернулся к точке $TAG."
echo "   Vercel передеплоит старую версию автоматически (~1 мин)."
echo "   На сайте обнови страницу через Cmd+Shift+R."
